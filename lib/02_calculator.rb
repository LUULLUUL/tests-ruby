def add(num1, num2)
  sum = num1 + num2
  return sum
end

def subtract(num1, num2)
  sum =  num1 - num2
  return sum
end
 
def sum(arr)
  sumup = arr.sum
  return sumup
end 

def multiply(num1, num2)
  sum = num1*num2
  return sum
end


//---

def power(num1, num2)
  sum = num1^num2
  return sum

def factorial(n)
  sum = 0
  while n >= 0
  sum = sum*n
  n -= 1
  end
  
  return sum
end  

